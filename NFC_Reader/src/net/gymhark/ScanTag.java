package net.gymhark;

public class ScanTag {
	private long id;
	private int schuelerid;
	private long time;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public int getschuelerid() {
		return schuelerid;
	}

	public void setschuelerid(int schuelerid) {
		this.schuelerid = schuelerid;
	}
	
	public long gettime() {
		return time;
	}
	

	public void settime(long time) {
		 this.time = time;
	}
}